import numpy as np

def build_random_array():
    a = np.random.random(10)
    np.random.rand(a)
    np.random.randint(1,10,20)
    np.random.randn(10)
    np.random.normal()
    np.random.uniform()
    np.random.seed()
    np.random.shuffle()
    np.random.choice()
